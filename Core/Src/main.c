/* USER CODE BEGIN 4 */
void TIM1_UEISR(void)
{
	  HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_14);  // Indicator

	  countSamples++;
	  if(countSamples > 99)
	  {
		  countSamples = 0;

		  flagHarfwave = 1;
	  }

	  buffPeriod = sineTable_10KHz[countSamples];
	  buffPeriod = (uint32_t)(buffPeriod * percentMod);

	  dutyCycle = buffPeriod / 100;

	  if(dutyCycle > MAX_FULLBRIDGE_PERIOD) dutyCycle = MAX_FULLBRIDGE_PERIOD;
	  if(dutyCycle < MIN_FULLBRIDGE_PERIOD) dutyCycle = MIN_FULLBRIDGE_PERIOD;

	  //TIM1->CCR1 = buffPeriod;
	  //TIM1->CCR2 = buffPeriod;

	  switch(fullBridgeState)
	  {
	  	  case 0:
	  	  {	   //TIM1->CCR3 = 0;
	  		   TIM1->CCR2 = 0;
	  		   TIM1->CCR1 = dutyCycle;

	  		  if(flagHarfwave == 1)
	  		  {
	  			flagHarfwave =0;
	  			fullBridgeState = 1;
	  		  }
	  	  }
	  	  break;


	  	  case 1:
	  	  {
	  		  	fullBridgeState = 2;
	  	  }
	  	  break;

	  	  case 2:
	  	  {
	  		  //TIM1->CCR3 = 0;
	  		  TIM1->CCR1 = 0;
	  		  TIM1->CCR2 = dutyCycle;

	  		  if(flagHarfwave == 1)
	  		  {
	  			flagHarfwave =0;
	  			fullBridgeState = 3;
	  		  }
	  	  }
	  	  break;

	  	  case 3:
	  	  {
	  		  	fullBridgeState = 0;
	  	  }
	  	  break;
	  }
}

